    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        if(this.estado && !this.validarPantalla()){
            long total = 0;
            
            switch(this.operacion){
                case 1:
                    total = this.previo + Long.parseLong(this.pantalla.getText());
                    break;
                case 2:
                    total = this.previo - Long.parseLong(this.pantalla.getText());
                    break;
                case 3:
                    total = this.previo * Long.parseLong(this.pantalla.getText());
                    break;
                case 4:
                    total = this.previo / Long.parseLong(this.pantalla.getText());
                    break;
                default:
                    break;
            }
            this.estado = false;
            
            this.pantalla.setText(String.valueOf(total));
        }
        
    }//GEN-LAST:event_jButton10ActionPerformed

