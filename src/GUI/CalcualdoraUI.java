    private void imprimirPantalla(String num){
        this.pantalla.setText(this.pantalla.getText()+num);
    }
    
    private boolean validarPantalla(){
        return this.pantalla.getText().isBlank();
    }

    private void asignarOperacion(int op){
        if(!this.validarPantalla() && !this.estado){
            this.previo = Long.parseLong(this.pantalla.getText());
            this.operacion = op;
            this.estado = true;
            this.pantalla.setText("");
        }
    }
